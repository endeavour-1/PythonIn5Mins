# and Operator (If both are true then only True)
price = 25
print(price>10 and price < 30)
#Output = True

# or Operator (At least one true then only True)
price = 5
print(price>10 or price < 30)
#Output = True


# not Operator (Inverses any value we give it. - If True the It'll give False)
price = 5
print(not price > 10)
#Output = True
